SET SERVEROUTPUT ON;
SET VERIFY OFF;

DECLARE
	N NUMBER:=&NUMBER;
BEGIN
	IF mod(N,2)=0 AND N!=0 THEN
		DBMS_OUTPUT.PUT_LINE(N||' IS A EVEN NUMBER');
	ELSIF MOD(N,2)!=0  AND N!=0 THEN
		DBMS_OUTPUT.PUT_LINE(N||' IS A ODD NUMBER');
	ELSE
		DBMS_OUTPUT.PUT_LINE(N||' IS NEITHER ODD NOR EVEN');
	END IF;
END;
/
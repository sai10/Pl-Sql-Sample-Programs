SET SERVEROUTPUT ON
SET VERIFY OFF
DECLARE

	VSAL NUMBER ;
	VENAME VARCHAR(20);
	VEMPNO NUMBER:=&EMPLOYEE_NUMBER;
BEGIN
	SELECT ENAME,SAL INTO VENAME,VSAL
	FROM EMP
	WHERE EMPNO=VEMPNO;
	DBMS_OUTPUT.PUT_LINE('NAME OF THE EMLOYEE NUMBER '||VEMPNO||'IS:'||VENAME);
	DBMS_OUTPUT.PUT_LINE('SALARY OF THE EMLOYEE NUMBER' ||VEMPNO||'IS:'||VSAL);
END;
